package edu.berkeley.diCal2.utility;

public class SimplePair<A,B> {

	private A first;
	private B second;
	
	public SimplePair(A first, B second)	{	
		this.first = first;	
		this.second = second;	
	}
	
	public A first()	{	
		return first;	
	}
	
	public B second()	{	
		return second;	
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((first == null) ? 0 : first.hashCode());
		result = prime * result + ((second == null) ? 0 : second.hashCode());
		return result;
	}

	@SuppressWarnings("unchecked")
	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		SimplePair<A,B> other = (SimplePair<A,B>) obj;
		if (first == null) {
			if (other.first != null)
				return false;
		} else if (!first.equals(other.first))
			return false;
		if (second == null) {
			if (other.second != null)
				return false;
		} else if (!second.equals(other.second))
			return false;
		return true;
	}

	@Override
	public String toString() {
		return "[" + first + "," + second + "]";
	}
	
	
}
